let num1: number = 10;
let num2: number = 5;
let result: number;

// Phép cộng
result = num1 + num2;
console.log("Kết quả của phép cộng:", result);

// Phép trừ
result = num1 - num2;
console.log("Kết quả của phép trừ:", result);

// Phép nhân
result = num1 * num2;
console.log("Kết quả của phép nhân:", result);

// Phép chia
result = num1 / num2;
console.log("Kết quả của phép chia:", result);

// Phép chia lấy phần dư
result = num1 % num2;
console.log("Kết quả của phép chia lấy phần dư:", result);
